function drawMap(room) {
    let rawMap = '   ____________________________\n  /  __________*1*__________  \\\n / /        ___|  |___        \\ \\\n' +
        '| |     /*7*__*8*__*9*\\     | |\n| |____/ / \\ \\______/ / \\ \\____| |\n*5*__*6*  *17*__*18*  *10*__*2*\n' +
        '| |  / /___ / /_  _\\ \\____\\ \\  | |\n| |*15*__*16*__*20*__*19*__*11*| |\n| |  \\ \\__  ___|  |___  __/ /  | |\n' +
        ' \\ \\  \\__*14*__*13*__*12*__/  / /\n  \\ \\____/ /__________\\ \\____/ /\n   \\___*4*___________*3*____/\n';
    for (let i = 1; i <= 20; i++)
        rawMap = rawMap.replace("*" + i + "*", (i === room ? "[" : " ") + (i < 10 ? " " : "") + i + (i === room ? "]" : " "));
    return rawMap;
}

console.log(drawMap(1));

